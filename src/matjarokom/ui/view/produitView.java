    public void FillDataStockLocal(){
        LocalStock.removeAllItems();
        Iterator<Stocke> itr=stockGetData.GetlistDataLocaleStock().iterator();
        Stocke next;
        while (itr.hasNext()) {
            next = itr.next();
            LocalStock.addItem(next.getLocale_Stocke());
        }
        itr=null;
        next=null;
    }
